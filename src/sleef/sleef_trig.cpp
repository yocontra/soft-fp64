// ========================================================================
// Forward trig — sin / cos / sincos / tan
// ========================================================================

extern "C" double sf64_sin(double d) {
    soft_fp64::sleef::sf64_internal_fe_acc fe;
    if (isnan_(d) || isinf_(d))
        return qNaN();
    const double absd = sf64_fabs(d);
    DD s_dd;
    int32_t ql;
    if (lt_(absd, kTRIGRANGEMAX2)) {
        const double qf = rint_(sf64_mul(d, 1.0 / kPI));
        ql = sf64_to_i32(qf);
        const double u = sf64_fma(qf, sf64_neg(kPI_A2), d);
        s_dd = ddadd_dd_d_d(u, sf64_mul(qf, sf64_neg(kPI_B2)));
    } else if (lt_(absd, kTRIGRANGEMAX)) {
        constexpr double kTwo24 = 16777216.0;
        constexpr double kTwo24Inv = 1.0 / 16777216.0;
        const double dqh = sf64_mul(trunc_(sf64_mul(d, sf64_mul(1.0 / kPI, kTwo24Inv))), kTwo24);
        const double qf = rint_(sf64_fma(d, 1.0 / kPI, sf64_neg(dqh)));
        ql = sf64_to_i32(qf);
        double u = sf64_fma(dqh, sf64_neg(kPI_A), d);
        DD s = ddadd_dd_d_d(u, sf64_mul(qf, sf64_neg(kPI_A)));
        s = ddadd2_dd_dd_d(s, sf64_mul(dqh, sf64_neg(kPI_B)));
        s = ddadd2_dd_dd_d(s, sf64_mul(qf, sf64_neg(kPI_B)));
        s = ddadd2_dd_dd_d(s, sf64_mul(dqh, sf64_neg(kPI_C)));
        s = ddadd2_dd_dd_d(s, sf64_mul(qf, sf64_neg(kPI_C)));
        s = ddadd_dd_dd_d(s, sf64_mul(sf64_add(dqh, qf), sf64_neg(kPI_D)));
        s_dd = s;
    } else {
        RempiResult rr = rempi_(d, fe);
        const int signhi = gt_(rr.dd.hi, 0.0) ? 1 : 0;
        ql = ((rr.q & 3) * 2 + signhi + 1) >> 2;
        DD s = rr.dd;
        if ((rr.q & 1) != 0) {
            const double sgn = gt_(rr.dd.hi, 0.0) ? 1.0 : -1.0;
            const double adj_h = sf64_mul(3.141592653589793116 * -0.5, sgn);
            const double adj_l = sf64_mul(1.2246467991473532072e-16 * -0.5, sgn);
            s = ddadd2_dd_dd(s, DD{adj_h, adj_l});
        }
        s_dd = ddnormalize_dd_dd(s);
    }

    // SLEEF u1 polynomial core: t=s; s=ddsqu(s); poly; x = 1 + (-1/6 + u*sx)*s;
    // result = ddmul_d(t, x); flip if ql & 1.
    DD t = s_dd;
    DD s = ddsqu_dd_dd(t);
    const double sx = s.hi;
    double u = poly_array(sx, kSinU1Poly6, sizeof(kSinU1Poly6) / sizeof(kSinU1Poly6[0]));
    u = sf64_fma(u, sx, kSinU1Tail);
    DD inner = ddadd_dd_d_d(kSinU1NegSixth, sf64_mul(u, sx));
    DD x = ddadd_dd_d_dd(1.0, ddmul_dd_dd_dd(inner, s));
    double r = ddmul_d_dd_dd(t, x);
    if ((ql & 1) != 0)
        r = sf64_neg(r);
    if (is_neg_zero(d))
        r = d;
    fe.flush();
    return r;
}

extern "C" double sf64_cos(double d) {
    soft_fp64::sleef::sf64_internal_fe_acc fe;
    if (isnan_(d) || isinf_(d))
        return qNaN();
    const double absd = sf64_fabs(d);
    DD s_dd;
    int32_t ql;
    if (lt_(absd, kTRIGRANGEMAX2)) {
        const double qf = rint_(sf64_fma(absd, 1.0 / kPI, -0.5));
        ql = 2 * sf64_to_i32(qf) + 1;
        DD s = ddadd2_dd_d_d(
            absd, sf64_mul(sf64_from_i64(static_cast<int64_t>(ql)), sf64_mul(-kPI_A2, 0.5)));
        s = ddadd_dd_dd_d(
            s, sf64_mul(sf64_from_i64(static_cast<int64_t>(ql)), sf64_mul(-kPI_B2, 0.5)));
        s_dd = s;
    } else if (lt_(absd, kTRIGRANGEMAX)) {
        // SLEEF `xcos_u1` extended Cody-Waite:
        //   dqh_small = trunc(d * (M_1_PI/2^23) - 0.5*(M_1_PI/2^23))   [2^23 units]
        //   ql        = 2*rint(d*M_1_PI - 0.5 - dqh_small*2^23) + 1
        //   dqh       = dqh_small * 2^24                                [now 2^24 units]
        // Keep the two scales separate — using dqh before the ×2^24 is
        // what lets the subtraction in `ql` cancel correctly.
        constexpr double kTwo23Inv = 1.0 / 8388608.0;
        constexpr double kTwo23 = 8388608.0;
        constexpr double kTwo24 = 16777216.0;
        const double dqh_small = trunc_(sf64_sub(sf64_mul(absd, sf64_mul(1.0 / kPI, kTwo23Inv)),
                                                 sf64_mul(0.5, sf64_mul(1.0 / kPI, kTwo23Inv))));
        ql = 2 * sf64_to_i32(rint_(
                     sf64_sub(sf64_fma(absd, 1.0 / kPI, -0.5), sf64_mul(dqh_small, kTwo23)))) +
             1;
        const double dqh = sf64_mul(dqh_small, kTwo24);

        double u = sf64_fma(dqh, sf64_mul(-kPI_A, 0.5), absd);
        DD s = ddadd2_dd_d_d(
            u, sf64_mul(sf64_from_i64(static_cast<int64_t>(ql)), sf64_mul(-kPI_A, 0.5)));
        s = ddadd2_dd_dd_d(s, sf64_mul(dqh, sf64_mul(-kPI_B, 0.5)));
        s = ddadd2_dd_dd_d(
            s, sf64_mul(sf64_from_i64(static_cast<int64_t>(ql)), sf64_mul(-kPI_B, 0.5)));
        s = ddadd2_dd_dd_d(s, sf64_mul(dqh, sf64_mul(-kPI_C, 0.5)));
        s = ddadd2_dd_dd_d(
            s, sf64_mul(sf64_from_i64(static_cast<int64_t>(ql)), sf64_mul(-kPI_C, 0.5)));
        s = ddadd_dd_dd_d(s, sf64_mul(sf64_add(dqh, sf64_from_i64(static_cast<int64_t>(ql))),
                                      sf64_mul(-kPI_D, 0.5)));
        s_dd = s;
    } else {
        RempiResult rr = rempi_(absd, fe);
        const int signhi = gt_(rr.dd.hi, 0.0) ? 1 : 0;
        ql = ((rr.q & 3) * 2 + signhi + 7) >> 1;
        DD s = rr.dd;
        if ((rr.q & 1) == 0) {
            const double sgn = gt_(rr.dd.hi, 0.0) ? 1.0 : -1.0;
            const double adj_h = sf64_mul(3.141592653589793116 * -0.5, sgn);
            const double adj_l = sf64_mul(1.2246467991473532072e-16 * -0.5, sgn);
            s = ddadd2_dd_dd(s, DD{adj_h, adj_l});
        }
        s_dd = ddnormalize_dd_dd(s);
    }

    DD t = s_dd;
    DD s = ddsqu_dd_dd(t);
    const double sx = s.hi;
    double u = poly_array(sx, kSinU1Poly6, sizeof(kSinU1Poly6) / sizeof(kSinU1Poly6[0]));
    u = sf64_fma(u, sx, kSinU1Tail);
    DD inner = ddadd_dd_d_d(kSinU1NegSixth, sf64_mul(u, sx));
    DD x = ddadd_dd_d_dd(1.0, ddmul_dd_dd_dd(inner, s));
    double r = ddmul_d_dd_dd(t, x);
    if ((ql & 2) == 0)
        r = sf64_neg(r);
    fe.flush();
    return r;
}

extern "C" void sf64_sincos(double d, double* s_out, double* c_out) {
    if (s_out)
        *s_out = sf64_sin(d);
    if (c_out)
        *c_out = sf64_cos(d);
}

extern "C" double sf64_tan(double d) {
    soft_fp64::sleef::sf64_internal_fe_acc fe;
    if (isnan_(d) || isinf_(d))
        return qNaN();
    const double absd = sf64_fabs(d);
    DD s_dd;
    int32_t ql;
    if (lt_(absd, kTRIGRANGEMAX2)) {
        const double qf = rint_(sf64_mul(d, k2_PI));
        ql = sf64_to_i32(qf);
        const double u = sf64_fma(qf, sf64_neg(kPI_A2_H), d);
        s_dd = ddadd_dd_d_d(u, sf64_mul(qf, sf64_neg(kPI_B2_H)));
    } else if (lt_(absd, kTRIGRANGEMAX)) {
        constexpr double kTwo24 = 16777216.0;
        constexpr double kTwo24Inv = 1.0 / 16777216.0;
        const double dqh = sf64_mul(trunc_(sf64_mul(d, sf64_mul(k2_PI, kTwo24Inv))), kTwo24);
        const double sgnhalf = (lt_(d, 0.0)) ? -0.5 : 0.5;
        DD s_q = ddadd2_dd_d_d(sf64_mul(k2_PI, d), sf64_sub(sgnhalf, dqh));
        double qf_d = sf64_add(s_q.hi, s_q.lo);
        qf_d = trunc_(qf_d);
        ql = sf64_to_i32(qf_d);

        double u = sf64_fma(dqh, sf64_neg(kPI_A_H), d);
        DD s = ddadd_dd_d_d(u, sf64_mul(qf_d, sf64_neg(kPI_A_H)));
        s = ddadd2_dd_dd_d(s, sf64_mul(dqh, sf64_neg(kPI_B_H)));
        s = ddadd2_dd_dd_d(s, sf64_mul(qf_d, sf64_neg(kPI_B_H)));
        s = ddadd2_dd_dd_d(s, sf64_mul(dqh, sf64_neg(kPI_C_H)));
        s = ddadd2_dd_dd_d(s, sf64_mul(qf_d, sf64_neg(kPI_C_H)));
        s = ddadd_dd_dd_d(s, sf64_mul(sf64_add(dqh, qf_d), sf64_neg(kPI_D_H)));
        s_dd = s;
    } else {
        RempiResult rr = rempi_(d, fe);
        ql = rr.q;
        s_dd = rr.dd;
    }

    // SLEEF xtan_u1 tangent formula via half-angle identity.
    DD s = s_dd;
    DD t = ddscale_dd_dd_d(s, 0.5);
    s = ddsqu_dd_dd(t);
    const double sx = s.hi;
    double u = +0.3245098826639276316e-3;
    u = sf64_fma(u, sx, +0.5619219738114323735e-3);
    u = sf64_fma(u, sx, +0.1460781502402784494e-2);
    u = sf64_fma(u, sx, +0.3591611540792499519e-2);
    u = sf64_fma(u, sx, +0.8863268409563113126e-2);
    u = sf64_fma(u, sx, +0.2186948728185535498e-1);
    u = sf64_fma(u, sx, +0.5396825399517272970e-1);
    u = sf64_fma(u, sx, +0.1333333333330500581e+0);
    u = sf64_fma(u, sx, +0.3333333333333343695e+0);

    DD x = ddadd_dd_dd_dd(t, ddmul_dd_dd_d(ddmul_dd_dd_dd(s, t), u));
    DD y = ddadd_dd_d_dd(-1.0, ddsqu_dd_dd(x));
    x = ddscale_dd_dd_d(x, -2.0);

    if ((ql & 1) != 0) {
        DD tmp = x;
        x = y;
        y = ddneg_dd_dd(tmp);
    }

    x = dddiv_dd_dd_dd(x, y);
    double r = sf64_add(x.hi, x.lo);
    if (is_neg_zero(d))
        r = d;
    fe.flush();
    return r;
}

// ---- sinpi / cospi / tanpi --------------------------------------------
//
// SLEEF upstream clamps at |x| > TRIGRANGEMAX3/4 (= 2.5e8) because past
// that a binary64 `x` has <1 bit of fractional precision left after the
// mod-2 reduction — the polynomial argument would be noise. We match that
// behaviour: > 2.5e8 ⇒ 0 for sinpi/tanpi, 1 for cospi.

namespace {
constexpr double kSinpiClamp = 2.5e8;
}

extern "C" double sf64_sinpi(double x) {
    soft_fp64::sleef::sf64_internal_fe_acc fe;
    if (isnan_(x) || isinf_(x))
        return qNaN();
    if (gt_(sf64_fabs(x), kSinpiClamp))
        return is_neg_zero(x) ? x : 0.0;
    DD r = sinpi_core(x, fe);
    double ret = sf64_add(r.hi, r.lo);
    if (is_neg_zero(x))
        ret = -0.0;
    fe.flush();
    return ret;
}

extern "C" double sf64_cospi(double x) {
    soft_fp64::sleef::sf64_internal_fe_acc fe;
    if (isnan_(x) || isinf_(x))
        return qNaN();
    if (gt_(sf64_fabs(x), kSinpiClamp))
        return 1.0;
    DD r = cospi_core(x, fe);
    const double ret = sf64_add(r.hi, r.lo);
    fe.flush();
    return ret;
}

extern "C" double sf64_tanpi(double x) {
    soft_fp64::sleef::sf64_internal_fe_acc fe;
    if (isnan_(x) || isinf_(x))
        return qNaN();
    if (gt_(sf64_fabs(x), kSinpiClamp))
        return 0.0;
    DD s = sinpi_core(x, fe);
    DD c = cospi_core(x, fe);
    const double ch = sf64_add(c.hi, c.lo);
    if (eq_(ch, 0.0)) {
        const double sh = sf64_add(s.hi, s.lo);
        fe.flush();
        return signbit_(sh) ? sf64_neg(kInf) : kInf;
    }
    DD r = dddiv_dd_dd_dd(s, c);
    const double ret = sf64_add(r.hi, r.lo);
    fe.flush();
    return ret;
}
